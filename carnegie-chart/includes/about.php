    <h3 class="center">About</h3>
    <p>
      This chart was designed to help you better yourself at the art of
      winning friends, influencing people, and overall just being a better communicator.
      It is based off of Dale Carnegie's
      <i>How to Win Friends and Influence People</i>, easily the best
      and most applicable self-improvement book ever written. You can use
      this chart as a 30-day practice plan to help you understand and enact
      upon some of the awesome suggestions in Carnegie's book.
    </p>
    <p>
      At first glance, the title of this book probably gives off a superficial vibe.
      After all, the thought off explicitly learning how to get better at "winning friends and influencing people"
      doesn't sound like like the most humble topic. But I can't tell you
      how humble the true meaning of this book is. Dale Carnegie
      genuinely provides you with the tools to create
      real, honest relationships with everyone you meet, and help you
      build on the ones you already have.
    </p>
    <p>
      His book has absolutely
      zero "tricks" to persuading or influencing others, just practical
      suggestions on how to enhance every interaction with those around
      you - as well as helping you to become a better person overall.
      To prove it to you, some of the covered topics include "Giving honest and sincere appreciation,"
      "Talk about your own mistakes before criticizing others," and
      "Praise every improvement, no matter how small." That doesn't sound
      so bad, does it?
    </p>
    <p>
      So, what do you have to lose? Give the chart a try and see if you notice
      a difference in your personal relationships with people you have
      known for a while and also with people you're just meeting!
    </p>
    <hr />
    <h3 class="center">How It Works</h3>
    <p>
      This chart is broken up into 6 weeks, each week with 5 days. Each of these days will be a day
      that you practice the listed skills, all of which are thoroughly
      discussed in <i>How to Win Friends and Influence People</i>.
      Obviously there are 7 days in a week, so you get 2 free days where
      you don't have to practice these skills (though you still should!).
      The idea is that you will practice these skills at work, which for
      most people is 5 days a week, and you can
      pick whichever 5 practice days you want every week.
      In addition to practicing at work, these skills are 100% practicable
      with your personal friends and family at home or at school.
    </p>
    <p>
      Each day will have anywhere from 1 to 3 skills for you to
      practice. There's no quantitative amount of practicing where you
      can say "Daily Mission Accomplished!," so just practice the daily
      skills until you feel comfortable with them. Each week is
      categorized in some way, and will be exercising a certain verbal
      skill set.
    </p>
    <p>
      If you truly stick with the daily skill goals, then I guarantee
      you will notice a positive difference among how your colleagues
      perceive and respect you. The only thing I ask of you is to give
      this chart an honest chance if you wish to succeed in the covered
      areas. Please don't half-ass it, or you probably won't see much
      progress. Remember, this isn't something like a school course that's
      worth cheating on in order to pass; it's to help you, and only
      you, so do your best!
    </p>
